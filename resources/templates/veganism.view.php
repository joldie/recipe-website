		<h1>Veganism!</h1>
		<h2>So, what is it?</h2>
		<p>
			<em>To quote the <a href="https://www.vegansociety.com/go-vegan/definition-veganism" target="_blank" rel="noopener">Vegan Society</a>:</em>
		</p>
		<blockquote cite="https://www.vegansociety.com/go-vegan/definition-veganism">
			Veganism is a way of living which seeks to exclude, as far as is possible and practicable, all forms of exploitation of, and cruelty to, animals for food, clothing or any other purpose
		</blockquote>
		<br>
		<h2>Okay, but why?</h2>
		<p>
			<em>Vegans simply think that...</em>
		</p>
		<div>
		<b>a)</b> causing unnecessary suffering to animals is unethical,
		<br>
		<b>b)</b> suffering is unavoidable in the production of all animal products, and
		<br>
		<b>c)</b> animal products these days are generally unnecessary for our health or survival
		</div>
		<p>
			<em>which leads to the logical conclusion that...</em>
		</p>
		<div>
			<b>&#8594;</b> it is unethical to produce (or support the production of) animal products unless absolutely necessary.
			</div>
		<p>
			<em>Or, put another way:</em>
		</p>
		<div class="center-align">
		<strong>If we <i>can</i> avoid harming animals, <i>why wouldn't we?</i></strong>
		</div>
